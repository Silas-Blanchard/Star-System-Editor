/**
 * JavaFX App
 */
public class App extends Application {

    private static Scene scene;

    @Override
    public void start(Stage stage) throws IOException, URISyntaxException {
        Sim sim = Sim.getSim();
        sim.set_window(stage);

        //Loading our plane from MainEditorControl.java simple as     
        Rectangle2D primaryScreenBounds = Screen.getPrimary().getVisualBounds();
        stage.setX(primaryScreenBounds.getMinX());
        stage.setY(primaryScreenBounds.getMinY());
        stage.setWidth(primaryScreenBounds.getWidth());
        stage.setHeight(primaryScreenBounds.getHeight());

        FXMLLoader loader = new FXMLLoader(getClass().getResource("mainmenu.fxml"));
        BorderPane content = loader.load();

        MainEditorControl controller = loader.getController();
        controller.setContent(); //helper method that adds everything we need

        Scene scene = new Scene(content);

        stage.setScene(scene);
        sim.set_window(stage);
        sim.set_scrollpane(controller.getScrollPane());

        stage.show();
    }

    static void setRoot(String fxml) throws IOException {
        scene.setRoot(loadFXML(fxml));
    }

    private static Parent loadFXML(String fxml) throws IOException {
        FXMLLoader fxmlLoader = new FXMLLoader(App.class.getResource(fxml + ".fxml"));
        return fxmlLoader.load();
    }

    public static void main(String[] args) {
        launch();
    }

}